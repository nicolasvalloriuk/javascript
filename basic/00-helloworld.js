console.log("5") // This will log the string "5" to the console
console.log(5) // This will log the number 5 to the console
console.log("5" + 5) // This will concatenate the string "5" with the number 5, resulting in "55"
console.log(5 + 2)  // This will perform addition, resulting in 7
console.log(5 - 2)  // This will perform subtraction, resulting in 3
console.log(5 * 2)  // This will perform multiplication, resulting in 10
console.log(5 / 2)  // This will perform division, resulting in 2.5
console.log(5 % 2)  // This will perform modulus operation, resulting in 1 (the remainder of 5 divided by 2)
console.log(5 ** 2) // This will perform exponentiation, resulting in 25 (5 raised to the power of 2)